/** Retorna a narração pelo ID do capítulo. */
export function getNarrationByChapter(
  chapterId: Narration['chapterId'],
): Narration | undefined {
  return NARRATIONS.find((n) => n.chapterId === chapterId)
}

/** Retorna a narração pelo ID exato da fala */
export function getNarrationById(id: string): Narration | undefined {
  return NARRATIONS.find((n) => n.id === id)
}
